# 1. Calculadora básica:
# Crie uma função para cada operação matemática básica (soma, subtração, 
# multiplicação e divisão) que recebe dois números como parâmetros e retorna o 
# resultado da operação.

def soma(x, y):
    return x + y
resultado = soma (10, 10)
print('A soma é:', resultado)   

def subtracao (x, y):
    return x - y
resultado = subtracao (20, 10)
print('A subtração é:', resultado)

def multiplicacao (x, y):
    return x * y
resultado = multiplicacao (10, 10)
print('A multiplicação é:', resultado)

def divisao (x, y):
    return x + y
resultado = divisao (10, 10)
print('A divisão é:', resultado)